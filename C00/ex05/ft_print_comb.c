#include <unistd.h>

void ft_print_comb(void)
{
	int i;
	int n;
	int x;

	i = 0;
	n = i + 1;
	x = n + 1;
	while(i<='7')
	{
		while(n<='8')
		{
			while(x<='9')
			{
				write(1,'i', 1);
				write(1,'n', 1);
				write(1,'x', 1);
				x++;
			}
			n++;
		}
		i++;
	}
}

int main(){ ft_print_comb();}
